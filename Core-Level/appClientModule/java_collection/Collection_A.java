package java_collection;

/**
 * Arrays VS Collections:
 * 
 * Arrays:
 * 
 * 1) Arrays are fixed in size and data stored in consecutive memory locations.
 * 
 * 2) Arrays can hold only Homogeneous data elements. But, we can solve this
 * problem using Object array.
 * 
 * 3) Arrays concept not built based on some data structure. Hence, ready-made
 * method support is not available for every requirement.
 * 
 * 4) Memory point of view arrays concept is not recommended to use.
 * 
 * 5) Arrays can be used to hold primitives and objects as well.
 * 
 * 
 * Collections over arrays:
 * 
 * 1) Collections are GrowablE in nature. Hence, based on our requirement we can
 * increase or decrease the size.
 * 
 * 2) Collection can hold both Homogeneous and Heterogeneous data elements.
 * 
 * 3) Every Collection class is implemented based on some data structure. Hence,
 * ready-made method support is available for every requirement.
 * 
 * 4) Memory point of collection concept is recommended to use.
 * 
 * 5) Collections can be used to hold only objects.
 * 
 * 
 * DisAdvantages of Collections:
 * 
 * Performance point of view collections are not recommended to use. This is the
 * limitation of collections. But, Performance point of view arrays are highly
 * recommended to use.
 * 
 * 
 * Collection:
 * 
 * If we want to represent a group of individual objects as a single entity,
 * then we should go for Collection interface.
 * 
 * Collection framework:
 * 
 * If we want to represent a group of individual objects as a single entity,
 * then several classes and interfaces required. This group of classes and
 * interfaces called Collection framework.
 * 
 * In the Collection framework the following are legacy characters:
 * 
 * Enumeration(I), Dictionary(abstract), Vector, Stack, Hashtable, Properties.
 */

